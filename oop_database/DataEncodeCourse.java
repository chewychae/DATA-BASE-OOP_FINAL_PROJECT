    private void textActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_textActionPerformed
        try {
            DB_connection.init();
            PreparedStatement ps = null;
            Connection c = DB_connection.getConnection();
            ResultSet rs;

            if (combo.getSelectedItem().equals("I.D")) {
                ps = c.prepareStatement("SELECT * FROM my.course WHERE ID LIKE '%" + text.getText() + "%'");
            } else if (combo.getSelectedItem().equals("Course_Code")) {
                ps = c.prepareStatement("Select * from my.course where Course_code LIKE '%" + text.getText() + "%'");
            } else if (combo.getSelectedItem().equals("Course_Title")) {
                ps = c.prepareStatement("SELECT * FROM my.course WHERE Course_title LIKE '%" + text.getText() + "%'");
            } else if (combo.getSelectedItem().equals("Instructor's ID")) {
                ps = c.prepareStatement("SELECT * FROM my.course WHERE INSTRUCTOR_ID LIKE '%" + text.getText() + "%'");
            } else {
                JOptionPane.showMessageDialog(this, "Please select a valid search criteria.");
                return;
            }

            rs = ps.executeQuery();
            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("ID"), rs.getString("Course_code"), rs.getString("Course_title"), rs.getString("INSTRUCTOR_ID")});

            }

        } catch (SQLException ex) {
            Logger.getLogger(DataEncodeCourse.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_textActionPerformed


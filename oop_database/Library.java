    void LoadData() {
        try {
            DB_connection.init();
            PreparedStatement ps;
            Connection c = DB_connection.getConnection();
            ResultSet rs;
            model.setRowCount(0);
            ps = c.prepareStatement("SELECT * FROM my.library;");
            rs = ps.executeQuery();
            
            while (rs.next()) {
                
                model.addRow(new Object[]{rs.getString("ID"), rs.getString("Book_title"), rs.getString("Book_type"),
                    rs.getString("Date_borrow"), rs.getString("Date_return"), rs.getString("STUDENTS_ID"), rs.getString("Book_ID")});
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(DataEncodeCourse.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
   


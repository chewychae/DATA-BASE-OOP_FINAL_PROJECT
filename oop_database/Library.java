    private void TextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TextActionPerformed
        try {
            DB_connection.init();
            PreparedStatement ps = null;
            Connection c = DB_connection.getConnection();
            ResultSet rs;
            
            if (combo.getSelectedItem().equals("ID")) {
                ps = c.prepareStatement("SELECT * FROM my.library WHERE ID LIKE '%" + Text.getText() + "%'");
            } else if (combo.getSelectedItem().equals("Book Title")) {
                ps = c.prepareStatement("SELECT * FROM my.library WHERE Book_title LIKE '%" + Text.getText() + "%'");
            } else if (combo.getSelectedItem().equals("Book Type")) {
                ps = c.prepareStatement("SELECT * FROM my.library WHERE Book_type LIKE '%" + Text.getText() + "%'");
            } else if (combo.getSelectedItem().equals("Date of Borrow")) {
                ps = c.prepareStatement("SELECT * FROM my.library WHERE Date_borrow LIKE '%" + Text.getText() + "%'");
            } else if (combo.getSelectedItem().equals("Date of Return")) {
                ps = c.prepareStatement("SELECT * FROM my.library WHERE Date_return LIKE '%" + Text.getText() + "%'");
            } else if (combo.getSelectedItem().equals("Student's ID")) {
                ps = c.prepareStatement("SELECT * FROM my.library WHERE STUDENTS_ID LIKE '%" + Text.getText() + "%'");
            } else if (combo.getSelectedItem().equals("Book's ID")) {
                ps = c.prepareStatement("SELECT * FROM my.library WHERE Book_ID LIKE '%" + Text.getText() + "%'");
            } else {
                JOptionPane.showMessageDialog(this, "Please select a valid search criteria.");
                return;
            }
            
            rs = ps.executeQuery();
            model.setRowCount(0);
            
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("ID"), rs.getString("Book_title"), rs.getString("Book_type"),
                    rs.getString("Date_borrow"), rs.getString("Date_return"), rs.getString("STUDENTS_ID"), rs.getString("Book_ID")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(DataEncode.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_TextActionPerformed

